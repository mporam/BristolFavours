<strong>Early Wedding Favours</strong>
<p>As early as the thirteenth century, couples have been giving favours to their wedding guests as a token of gratitude for sharing their special day and the beginning of the new life ahead of them. Throughout history and across every culture, marriage is viewed as a wonderful event to be celebrated. In many societies, everything touched by the happy couple was even thought to be charmed, and by giving <a href="gifts.php">gifts</a> to their wedding guests, these same blessings would be passed on.</p>

<p>For most modern couples, choosing the right favours is a vital part of creating the perfect wedding day. There are choices available to suit every theme, colour scheme and style, and options for every budget. Traditionally, though, selecting the wedding favours was a much simpler affair, as certain items were associated with more specific significance.</p>

<p>Early wedding favours, or <a href="products.php?cat=44">bonbonnieres</a>, originated with wealthier couples as small, fancy <a href="products.php?cat=12">boxes</a>, made from crystal, porcelain or gold, and were sometimes encrusted with precious stones. Inside the boxes were usually sugar cubes or delicate confections, which symbolised wealth and royalty. Sugar was expensive and highly valued as it was believed to have medicinal benefits.</p>

<p>As the price of sugar decreased, the tradition of giving favours to wedding guests became more affordable and was adopted by couples from more modest backgrounds. Bonbonnieres were replaced with almonds coated with sugar, known as &lsquo;confetti&rsquo; – from the Italian word meaning &lsquo;confectionary&rsquo;.  Five Jordan almonds were presented to each guest in a box or wrapped in fine fabric, each almond representing fertility, longevity, wealth, health and happiness. The tradition of giving sugared or chocolate coated almonds can be seen in many countries across the world, including Greece, Italy and the Middle East. The bitterness of the almond with the sweetness of the coating represent the bitter sweetness of a marriage.</p>

<p>Over time, different favours were introduced. In the sixteenth and seventeenth centuries, for instance, bridal couples gave out love knots made from lace and <a href="products.php?cat=20">ribbons</a>. Gloves were also given to wedding guests, to symbolise extending the hand of friendship. Though the range of different favours available has vastly increased, many of the oldest traditions are still upheld today.</p>

<p>We are all familiar with the famous old wedding rhyme:</p>

<center><p><i>Something old, something new<br />
Something borrowed, something blue<br />
And a silver sixpence in her shoe</i></p></center>

<p>Each item in the poem represents a good-luck token for the bride. The belief is that if she carries all of these items on her wedding day, her marriage will be happy.</p>

<p>The &lsquo;Something old&rsquo; symbolises continuity with the bride&rsquo;s family and past, and also protection for any children to come. &lsquo;Something new&rsquo; represents optimism for the bride&rsquo;s new life with her husband. &lsquo;Something borrowed&rsquo; is normally an item from a married friend or family member, whose good fortune in marriage is passed on to the new bride. It is also intended to remind the bride that she can always depend on her friends and family.</p>

<p>The colour blue has been important in weddings for centuries – in ancient Rome, brides were dressed in blue to symbolise love, modesty and fidelity. Prior to the late nineteenth century it was commonplace for wedding gowns to be blue. In Christianity, the Virgin Mary is usually depicted in blue, so the colour is also often associated with purity. </p>

<strong>Walking on Silver</strong>

<p>The tradition of placing a <a href="proddetail.php?prod=197%2F1">silver sixpence</a> in the bride&rsquo;s shoe is thought to have originated in the late nineteenth century, in Lancashire. The coin was slipped into the shoe to ensure the future wealth and financial security of the couple. It was also thought to protect against any evil done by frustrated suitors. There is also a similar Scottish custom of the groom putting a silver coin under his foot for good luck. Today it is common practice to place a silver sixpence into the shoe to continue the tradition of &lsquo;walking on silver&rsquo;. It is the colour of the coin, rather than the value that is important, but for optimum fortune, the coin should be in the left shoe.</p>

<strong>Confetti</strong>

<p>Though in Italy, the word &lsquo;confetti&rsquo; refers to sugared almonds or candied fruits, in both English and French, the word &lsquo;confetti&rsquo; is used to mean the sprinkling of paper or petals thrown at weddings. The tradition of throwing <a href="proddetail.php?prod=197%2F1">confetti</a> comes from pagan times, when newlyweds had grain thrown over them, in the hopes that their marriage would be as fruitful as the seeds. It is also a way of showing the couple that you want to &lsquo;shower&rsquo; them with blessings for their new life together. </p>

